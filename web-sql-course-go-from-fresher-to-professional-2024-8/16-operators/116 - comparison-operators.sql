-- Comparison Operators
/*
Comparison
Operators	Description	
~~~~~~~~	~~~~~~~~~~~
	=		Equal to	
	>		Greater than	
	<		Less than	
	>=		Greater than or equal to	
	<=		Less than or equal to	
	<>		Not equal to
    
    
Condition: value1 comparison_operator value2 -> True/false
~~~~~~~~~
    
3 = 3 -> True
3 = 2 -> False
34 = 34 -> True
7 = 98 -> False


3 > 4 -> False
4 > 3 -> True
34 > 23 -> True
23 > 34 -> False

3 < 4 -> True
4 < 3 -> False
34 < 23 -> False
23 < 34 -> True


3 >= 4 -> False
3 >= 3 -> True
23 >= 45 -> False
45 >= 23 -> True

3 <= 4 -> True
3 <= 3 -> True
23 <= 45 -> True
45 <= 23 -> False


Not equal: 
3 <> 4 -> True 3 not equal 4
3 <> 3 -> False 3 not equal 3
23 <> 45 -> True 23 not equal 45
45 <> 23 -> True 45 not equal 23

*/


