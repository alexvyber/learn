-- Important Points

Point 1: The INDEX requires its own space in the hard disk.

			Database: space in the harddisk -> Tables
			Table: space in the harddisk -> Data
			Index: space in the harddisk -> Index
            
Point 2: 2.1 The disadvantage of index, it slow down the execution time of UPDATE and INSERT statements. 
		 2.2 But advantage is speed up the execution time of SELECT and WHERE statements.

Point 3: Avoid index on column names which contains large number of NULL values.


